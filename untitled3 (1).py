def parse_pdb(pdb_content):
    """Parses PDB string content and returns structural statistics."""
    parser = PDBParser(QUIET=True)
    pdb_file = StringIO(pdb_content)
    try:
        structure = parser.get_structure("molecule", pdb_file)
    except Exception as e: # Catch any exception during structure parsing
        # Re-raise as PDBConstructionException for consistent error handling
        raise PDBConstructionException(f"Failed to parse PDB structure: {e}") from e

    stats = {"models": 0, "chains": 0, "residues": 0, "atoms": 0}
    for model in structure:
        stats["models"] += 1
        for chain in model:
            stats["chains"] += 1
            for residue in chain:
                stats["residues"] += 1
                for atom in residue:
                    stats["atoms"] += 1
    return stats

def parse_sdf(sdf_content):
    """Parses SDF string content into a list of RDKit molecule objects."""
    suppl = Chem.SDMolSupplier()
    suppl.SetData(sdf_content, removeHs=False)
    mols = [m for m in suppl if m is not None]
    return mols


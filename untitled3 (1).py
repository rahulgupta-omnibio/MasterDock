def parse_pdb(pdb_content):
    """Parses PDB string content and returns structural statistics."""
    parser = PDBParser(QUIET=True)
    pdb_file = StringIO(pdb_content)
    try:
        structure = parser.get_structure("molecule", pdb_file)
    except Exception as e: # Catch any exception during structure parsing
        # Re-raise as PDBConstructionException for consistent error handling
        raise PDBConstructionException(f"Failed to parse PDB structure: {e}") from e

    stats = {"models": 0, "chains": 0, "residues": 0, "atoms": 0}
    for model in structure:
        stats["models"] += 1
        for chain in model:
            stats["chains"] += 1
            for residue in chain:
                stats["residues"] += 1
                for atom in residue:
                    stats["atoms"] += 1
    return stats

def parse_sdf(sdf_content):
    """Parses SDF string content into a list of RDKit molecule objects."""
    suppl = Chem.SDMolSupplier()
    suppl.SetData(sdf_content, removeHs=False)
    mols = [m for m in suppl if m is not None]
    return mols

def render_molecule_3d(file_content, file_type, width=800, height=400):
    """Generates HTML for py3Dmol visualization."""
    view = py3Dmol.view(width=width, height=height)
    view.addModel(file_content, file_type.lower())
    view.setStyle({'stick': {}})
    view.zoomTo()
    return view._repr_html_()

def render_molecule_2d(mol):
    """Generates a 2D PIL image from an RDKit molecule."""
    if mol is None:
        return None
    return Draw.MolToImage(mol, size=(300, 300))

def convert_to_pdbqt(file_content, input_format, molecule_type="small_molecule"):
    """
    Converts molecular file content (PDB or SDF) to PDBQT format using OpenBabel.
    Includes RDKit-based cleaning for small molecules prior to OpenBabel conversion.

    Args:
        file_content (str): The content of the input molecular file (PDB or SDF).
        input_format (str): The format of the input file ('pdb' or 'sdf').
        molecule_type (str): The type of molecule ('small_molecule', 'protein', 'dna', 'rna').

    Returns:
        str: The converted content in PDBQT format, or None if conversion fails.
    """
    if input_format.lower() not in ["pdb", "sdf"]:
        st.error(f"Unsupported input format for PDBQT conversion: {input_format}. Only PDB and SDF are supported.")
        return None

    original_input_filepath = None
    processed_input_filepath = None
    output_filepath = None

    try:
        # Create an initial temporary file for the raw input content
        with tempfile.NamedTemporaryFile(mode='w', delete=False, suffix=f'.{input_format}') as input_tmp:
            input_tmp.write(file_content)
            original_input_filepath = input_tmp.name

        current_input_filepath = original_input_filepath

        # --- RDKit Cleaning Steps for Ligands (SDF, small_molecule) ---
        if input_format.lower() == "sdf" and molecule_type == "small_molecule":
            st.info("Performing RDKit-based cleaning for small molecule ligand...")
            cleaned_mols = []
            suppl = Chem.SDMolSupplier()
            # Use a BytesIO object if file_content is bytes, or StringIO if it's string
            # Assuming file_content is a string from st.file_uploader.read().decode("utf-8")
            # For MolSupplier, setting data directly from string is often fine.
            suppl.SetData(file_content, removeHs=False) # Read original content

            remover = SaltRemover.SaltRemover() # Initialize salt remover

            for mol in suppl:
                if mol is not None:
                    # 1. Remove salts
                    mol_cleaned = remover.StripMol(mol) # StripMol returns a new mol if salts removed, else original
                    # 2. Add hydrogens consistently (e.g., for standard protonation states)
                    mol_cleaned = Chem.AddHs(mol_cleaned, addRemoveHs=True, explicitOnly=False)
                    cleaned_mols.append(mol_cleaned)

            if cleaned_mols:
                # Write cleaned molecules to a new temporary SDF file for obabel
                with tempfile.NamedTemporaryFile(mode='w', delete=False, suffix='.sdf') as cleaned_sdf_tmp:
                    writer = Chem.SDWriter(cleaned_sdf_tmp.name)
                    for mol in cleaned_mols:
                        writer.write(mol)
                    writer.close()
                    processed_input_filepath = cleaned_sdf_tmp.name
                current_input_filepath = processed_input_filepath
                st.success("Small molecule ligand cleaned with RDKit.")
            else:
                st.warning("No valid molecules found after RDKit cleaning. Proceeding with original content for OpenBabel.")

        # Determine output filepath based on the (potentially cleaned) input file
        output_filepath = current_input_filepath.replace(f'.{input_format}', '.pdbqt')

        command = [] # Initialize command list

        if molecule_type == "small_molecule":
            # For small molecules, OpenBabel with charge perception is generally sufficient.
            # -xj: perceive and add Gasteiger charges. This is crucial for Vina.
            # -xr: remove hydrogens (if not already handled by RDKit, obabel will do it).
            command = ["obabel", f"-i{input_format}", current_input_filepath, "-o", "pdbqt", "-O", output_filepath, "-xj", "-xr"]
        elif molecule_type == "protein":
            # --- IMPORTANT: Conceptual Placeholder for Robust Protein Preparation ---
            # For robust protein receptor preparation, specialized tools like AutoDockTools'
            # `prepare_receptor4.py` are highly recommended for optimal Vina performance.
            # These tools handle protonation states, non-standard residues, and proper
            # charge assignments more accurately than general-purpose converters like OpenBabel.
            st.warning(f"For protein receptor preparation, specialized tools like AutoDockTools' `prepare_receptor4.py` are often recommended. The current conversion using OpenBabel is a simplified demonstration and may not be sufficient for accurate docking.")
            command = ["obabel", f"-i{input_format}", current_input_filepath, "-o", "pdbqt", "-O", output_filepath, "-xj", "-xp"] # -xp adds partial charges, better than -xr for receptor
        elif molecule_type in ["dna", "rna"]:
            # --- IMPORTANT: Conceptual Placeholder for Robust Nucleic Acid Preparation ---
            # For robust nucleic acid preparation, specific protocols and dedicated tools
            # (e.g., AmberTools, or careful manual processing for protonation and charge)
            # are typically needed before converting to PDBQT. OpenBabel provides a basic
            # conversion but may not be sufficient for accurate docking with nucleic acids.
            st.warning(f"For {molecule_type} preparation, specific protocols and dedicated tools are typically needed. The current conversion using OpenBabel is a simplified demonstration and may not be sufficient for accurate docking.")
            command = ["obabel", f"-i{input_format}", current_input_filepath, "-o", "pdbqt", "-O", output_filepath, "-xj", "-xp"] # -xp adds partial charges

        if not command: # If command was not set, something went wrong with molecule_type
             st.error(f"Invalid molecule type: {molecule_type} for PDBQT conversion.")
             return None

        result = subprocess.run(command, capture_output=True, text=True, check=True)

        if os.path.exists(output_filepath):
            with open(output_filepath, 'r') as f:
                pdbqt_content = f.read()
            return pdbqt_content
        else:
            st.error("OpenBabel did not generate the PDBQT output file. Check input format and content.")
            return None

    except subprocess.CalledProcessError as e:
        st.error(f"Error during OpenBabel conversion: {e}")
        st.error(f"Stdout: {e.stdout}")
        st.error(f"Stderr: {e.stderr}")
        return None
    except FileNotFoundError:
        st.error("OpenBabel 'obabel' command not found. Please ensure it is installed and in PATH.")
        return None
    except Exception as e:
        st.error(f"An unexpected error occurred during PDBQT conversion: {e}")
        return None
    finally:
        # Clean up all temporary files that might have been created
        if original_input_filepath and os.path.exists(original_input_filepath):
            os.remove(original_input_filepath)
        if processed_input_filepath and os.path.exists(processed_input_filepath):
            os.remove(processed_input_filepath)
        if output_filepath and os.path.exists(output_filepath):
            os.remove(output_filepath)

def run_autodock_vina(receptor_pdbqt_path, ligand_pdbqt_path, config_file_path, output_path="output.pdbqt", log_path="vina_log.txt"):
    """
    Function to run AutoDock Vina and parse its log output.
    Assumes Vina executable is in PATH or specified with full path.

    Args:
        receptor_pdbqt_path (str): Path to the prepared receptor PDBQT file.
        ligand_pdbqt_path (str): Path to the prepared ligand PDBQT file.
        config_file_path (str): Path to the Vina configuration file (defines binding site, etc.).
        output_path (str): Path where Vina will save the docked poses.
        log_path (str): Path where Vina will save the log/score file.

    Returns:
        tuple: (bool, pd.DataFrame or str) - True if successful and DataFrame of scores,
               False and error message otherwise.
    """
    try:
        abs_output_path = os.path.abspath(output_path)
        abs_log_path = os.path.abspath(log_path)

        command = [
            "vina",
            "--receptor", receptor_pdbqt_path,
            "--ligand", ligand_pdbqt_path,
            "--config", config_file_path,
            "--out", abs_output_path,
            "--log", abs_log_path
        ]

        result = subprocess.run(
            command,
            capture_output=True,
            text=True,
            check=True
        )

        if os.path.exists(abs_log_path):
            with open(abs_log_path, 'r') as f:
                log_content = f.read()

            scores = []
            for line in log_content.splitlines():
                match = re.match(r'\s*(\d+)\s+([-\d.]+)\s+([-\d.]+)\s*$', line.strip())
                if match:
                    scores.append({
                        'Mode': int(match.group(1)),
                        'Binding Affinity (kcal/mol)': float(match.group(2)),
                        'RMSD (A)': float(match.group(3))
                    })

            if scores:
                df_scores = pd.DataFrame(scores)
                return True, df_scores
            else:
                return False, "Vina log file parsed but no scores found. Check Vina output format."
        else:
            return False, "Vina log file not generated. Vina might have failed silently or not started."

    except subprocess.CalledProcessError as e:
        return False, f"Vina command failed with error code {e.returncode}. Stderr: {e.stderr}. Stdout: {e.stdout}"
    except FileNotFoundError:
        return False, "Vina executable not found. Please ensure it is installed and in your system's PATH."
    except Exception as e:
        return False, f"An unexpected error occurred while running Vina: {e}"

def calculate_rmsd(ref_mol, probe_mol):
    """
    Calculates the RMSD between two RDKit molecules.
    Assumes both molecules have a conformer.
    """
    if ref_mol is None or probe_mol is None:
        return None
    try:
        # Add hydrogens explicitly for more accurate conformer generation and RMSD
        ref_mol_h = Chem.AddHs(ref_mol)
        probe_mol_h = Chem.AddHs(probe_mol)

        # Embed molecules with 3D coordinates if they don't have them
        if ref_mol_h.GetNumConformers() == 0:
            AllChem.EmbedMolecule(ref_mol_h, AllChem.ETKDGv2())
        if probe_mol_h.GetNumConformers() == 0:
            AllChem.EmbedMolecule(probe_mol_h, AllChem.ETKDGv2())

        # If embedding still fails, return None or raise error
        if ref_mol_h.GetNumConformers() == 0 or probe_mol_h.GetNumConformers() == 0:
            st.warning("Could not generate 3D conformers for RMSD calculation.")
            return None

        # Align the probe molecule to the reference molecule and get the RMSD
        # For redocking, we expect identical molecules, so direct alignment by atom index is fine
        rmsd = AllChem.AlignMol(probe_mol_h, ref_mol_h)
        return rmsd
    except Exception as e:
        st.error(f"Error calculating RMSD: {e}")
        return None


def parse_sdf_file(sdf_content):
    """Parses SDF string content and returns a list of RDKit molecule objects."""
    # Use a MolSupplier to read molecules from the SDF content string
    suppl = Chem.SDMolSupplier()
    suppl.SetData(sdf_content, removeHs=False) # Keep hydrogens if present

    molecules = []
    for mol in suppl:
        if mol is not None:
            molecules.append(mol)
    return molecules

